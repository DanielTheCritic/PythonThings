# Declaring a list
newList = [5, 10, 99, 8, 13]

print(f'New List: {newList}')

# Sorting
newList.sort()
print(f'Sorted list: {newList}')

newList.reverse()
print(f'Reversed list: {newList}')

# Adding
newList.append(78)
print(f'With added value: {newList}')

# Removing
newList.remove(5)
print(f'With removed value: {newList}')
